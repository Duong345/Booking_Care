let getTopDoctorHome = (limitInput) => {
  return new Promise(async (resolve, reject) => {
    try {
      let users = await db.User.findAll({
        limit: limitInput,
        where: { roleId: "R2" },
        order: [["createdAt", "DESC"]],
        attributes: {
          exclude: ["password"],
        },
        include: [
          {
            model: db.Allcode,
            as: "positionData",
            attributes: ["valueEn", "valueVi"],
          },
          {
            model: db.Allcode,
            as: "genderData",
            attributes: ["valueEn", "valueVi"],
          },
        ],
        distinct: true,
        raw: false,
        nest: true,
      });
      if (users && users.length > 0) {
        users = _.uniqBy(users, "id");
        // ensure each record's image field is a usable string (data URI)
        users = users.map((u) => {
          if (u.image) {
            if (typeof u.image !== "string") {
              // sometimes Sequelize returns Buffer-like object
              if (u.image.data) {
                u.image = Buffer.from(u.image.data).toString("base64");
              } else {
                u.image = u.image.toString();
              }
            }
            if (!u.image.startsWith("data")) {
              u.image = `data:image/jpeg;base64,${u.image}`;
            }
          }
          return u;
        });
      }
      resolve({
        errCode: 0,
        data: users,
      });
    } catch (e) {
      reject(e);
    }
  });
};
